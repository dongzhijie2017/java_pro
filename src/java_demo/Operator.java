public class Operator {
	
	public static void main(String[] args) {
		//算数运算符
		int a=5;
		int b=6;
		//求和
		int sum=a+b;
		//减法
		int sub=b-a;
		//乘法
		int mul=a*b;
		//除法
		int dvd=a/b;
		//求余
		int mod=a%b;
		
		System.out.println(sum);
		System.out.println(sub);
		System.out.println(mul);
		System.out.println(dvd);
		System.out.println(mod);

		
	}

}
